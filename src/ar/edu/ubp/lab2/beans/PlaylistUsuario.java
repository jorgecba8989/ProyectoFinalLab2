package ar.edu.ubp.lab2.beans;

public class PlaylistUsuario {
		
	private Integer nroLista;
	private String nomLista;
	private Integer nroUsuario;
	
	
	public Integer getNroLista() {
		return nroLista;
	}
	public void setNroLista(Integer nroLista) {
		this.nroLista = nroLista;
	}
	public String getNomLista() {
		return nomLista;
	}
	public void setNomLista(String nomLista) {
		this.nomLista = nomLista;
	}
	public Integer getNroUsuario() {
		return nroUsuario;
	}
	public void setNroUsuario(Integer nroUsuario) {
		this.nroUsuario = nroUsuario;
	}
	
	
}
