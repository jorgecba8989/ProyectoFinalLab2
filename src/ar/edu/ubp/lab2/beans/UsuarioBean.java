package ar.edu.ubp.lab2.beans;

public class UsuarioBean {
	
	private Integer nroUsuario;
	private String nomUsuario;
	private String apellidoUsuario;
	private String veriClaveUsuario;
	private String claveUsuario;
	private String emailUsuario;
	private String aliasUsuario;
	
	
	public Integer getNroUsuario() {
		return nroUsuario;
	}
	public void setNroUsuario(Integer nroUsuario) {
		this.nroUsuario = nroUsuario;
	}
	public String getNomUsuario() {
		return nomUsuario;
	}
	public void setNomUsuario(String nomUsuario) {
		this.nomUsuario = nomUsuario;
	}
	public String getClaveUsuario() {
		return claveUsuario;
	}
	public void setClaveUsuario(String claveUsuario) {
		this.claveUsuario = claveUsuario;
	}
	public String getEmailUsuario() {
		return emailUsuario;
	}
	public void setEmailUsuario(String emailUsuario) {
		this.emailUsuario = emailUsuario;
	}
	public String getApellidoUsuario() {
		return apellidoUsuario;
	}
	public void setApellidoUsuario(String apellidoUsuario) {
		this.apellidoUsuario = apellidoUsuario;
	}
	public String getVeriClaveUsuario() {
		return veriClaveUsuario;
	}
	public void setVeriClaveUsuario(String veriClaveUsuario) {
		this.veriClaveUsuario = veriClaveUsuario;
	}
	public String getAliasUsuario() {
		return aliasUsuario;
	}
	public void setAliasUsuario(String aliasUsuario) {
		this.aliasUsuario = aliasUsuario;
	}
	
	
}
